#' Dynamics of Baltic Sea ecosystem
#'
#' A dataset containing time series of multiple components of 
#' the Gotland basin ecosystem in spring between 1980 and 2011. 
#'
#' @format A data frame with 32 rows (corresponding to 32 years) and 10 variables:
#' \describe{
#'   \item{Cod}{Cod (Gadus morhua) spawner biomass, in tonnes}
#'   \item{Herring}{Herring (Clupea harengus) spawner biomass, in tonnes}
#'   \item{Sprat}{Sprat (Sprattus sprattus) spawner biomass, in tonnes}
#'   \item{Acartia}{Acartia spp. biomass in Gotland basin in spring, in mg.m-3}
#'   \item{Pseudocalanus}{Pseudocalanus acuspes biomass in Gotland basin in spring, in mg.m-3}
#'   \item{Temora}{Temora longicornis biomass in Gotland basin in spring, in mg.m-3}
#'   \item{Chla}{Chlorophyll a concentration in Gotland basin in spring, in mg.m-3}
#'   \item{Diatom}{Diatoms in Gotland basin in spring, in mg.m-3}
#'   \item{Dinoflagellate}{Dinoflagellates concentration in Gotland basin in spring, in mg.m-3}
#'   \item{Cyanobacteria}{Cyanobacteria in Gotland basin in spring, in mg.m-3}
#' }
#' @source ICES/HELCOM Working Group on Integrated Assessments of the Baltic Sea \url{https://www.ices.dk/community/groups/Pages/WGIAB.aspx}
#' @references Möllmann C., Diekmann R. , Müller-Karulis B. , Kornilovs G. , Plikshs M. and Axe P. (2009), Reorganization of a large marine ecosystem due to atmospheric and anthropogenic pressure: a discontinuous regime shift in the Central Baltic Sea. Global Change Biology, 15: 1377-1393. doi:10.1111/j.1365-2486.2008.01814.x

